package java_Day_17;

public class WhileLoop2 {
	public static void main(String[] args) {
		int num = 0;
		
		while (num < 5) {
			System.out.println("Java is fun!");
			++num;
		}
		
		
		//Java is fun runs 5 times because the condition is 
		//as long as num is less than 5 print Java is fun
		
		
		
	}
}
